import Foundation
import UIKit

extension UIColor {
    static var sunnyAppColor: UIColor {
        return UIColor(named: "sunnyColour")!
    }
    static var cloudyAppColor: UIColor {
        return UIColor(named: "cloudyColour")!
    }
    static var rainyAppColor: UIColor {
        return UIColor(named: "rainyColour")!
    }
}
